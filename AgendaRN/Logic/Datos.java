package AgendaRN.Logic;

public class Datos {
    public int orden;
    public String nombre;
    public String apellido;
    public String ciudad;

    public Datos(int orden, String nombre, String apellido,String ciudad){
        this.orden = orden;
        this.nombre = nombre;
        this.apellido = apellido;
        this.ciudad = ciudad;
    }
}
